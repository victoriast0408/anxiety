// Function to populate content dynamically
function populateContent(day) {
    const content = courseContent.days.find(d => d.day === day);

    if (!content) return;

    const mediaCard = document.getElementById("media");
    const taskCard = document.getElementById("task");
    const reflectionCard = document.getElementById("reflection");

    if (day === 8) {
        // Special handling for "–ó–∞–≤–µ—Ä—à–µ–Ω–Ω—è –∫—É—Ä—Å—É"
        mediaCard.innerHTML = `
            <h2 class="day-title">${content.title}</h2>
            <div class="completion-text">
                <p>${content.description}</p>
            </div>
        `;
        taskCard.style.display = "none"; // Hide task card
        reflectionCard.style.display = "none"; // Hide reflection card
    } else {
        // Update media section
        mediaCard.innerHTML = `
            <h2 class="day-title">${content.title}</h2>
            <p>${content.description}</p>
            <iframe src="${content.videos[0].src}" title="${content.videos[0].title}" frameborder="0" allowfullscreen></iframe>
            <p><strong>–¢–µ–ø–µ—Ä –ø–µ—Ä–µ—Ö–æ–¥–∏–º–æ –¥–æ –ø—Ä–∞–∫—Ç–∏–∫–∏ üëá</strong></p>
            ${content.videos.slice(1).map(video => `
                <iframe src="${video.src}" title="${video.title}" frameborder="0" allowfullscreen></iframe>
            `).join('')}
        `;

        // Update task section
        taskCard.style.display = "block";
        taskCard.innerHTML = `
            <h3>${content.task.badge}</h3>
            <ol>
                ${content.task.steps.map(step => {
                    if (Array.isArray(step)) {
                        return `<ul>${step.map(subStep => `<li>${subStep}</li>`).join('')}</ul>`;
                    } else {
                        return `<li>${step}</li>`;
                    }
                }).join('')}
            </ol>
        `;

        // Update advice section
        reflectionCard.style.display = "block";
        reflectionCard.innerHTML = `<p>${content.advice}</p>`;
    }
}

// Example: Populate content for Day 1
populateContent(1);
